#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	int		size;
	char	*res;

	if (!s)
		return (0);
	i = 0;
	size = ft_strlen(s);
	if (!(res = (char *)malloc(sizeof(char) * size + 1)))
		return (0);
	while (s[i])
	{
		res[i] = (*f)(i, s[i]);
		i++;
	}
	res[i] = 0;
	return (res);
}
